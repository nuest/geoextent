class TestPointCloudHandlerConflicts:
    """Test that LAS/LAZ files are not claimed by other handlers"""

    def test_las_not_handled_by_raster(self):
        """LAS files should not be claimed by the raster handler."""
        assert not handle_raster.check_file_supported(
            "tests/testdata/pointcloud/wgs84.las"
        )

    def test_las_not_handled_by_vector(self):
        """LAS files should not be claimed by the vector handler."""
        assert not handle_vector.check_file_supported(
            "tests/testdata/pointcloud/wgs84.las"
        )

    def test_pointcloud_handler_claims_las(self):
        """The pointcloud handler should claim LAS files."""
        assert handle_pointcloud.check_file_supported(
            "tests/testdata/pointcloud/wgs84.las"
        )

    def test_pointcloud_handler_claims_laz(self):
        """The pointcloud handler should claim LAZ files."""
        assert handle_pointcloud.check_file_supported(
            "tests/testdata/pointcloud/wgs84.laz"
        )

    def test_pointcloud_handler_rejects_geojson(self):
        """The pointcloud handler should reject non-LAS files."""
        assert not handle_pointcloud.check_file_supported(
            "tests/testdata/muenster_ring_zeit.geojson"
        )


class TestPointCloudInDirectory:
    """Test that LAS files are discovered during directory scanning"""

    def test_pointcloud_in_directory(self):
        """Directory scanning picks up LAS files."""
        result = geoextent.from_directory(
            "tests/testdata/pointcloud",
            bbox=True,
            tbox=True,
            details=True,
            show_progress=False,
        )
        assert result is not None
        assert "bbox" in result
        assert "tbox" in result
        # Should have found at least one file with an extent
        assert result["bbox"] is not None
        # Check details for individual files
        details = result.get("details", {})
        assert any(k.endswith(".las") or k.endswith(".laz") for k in details.keys())


